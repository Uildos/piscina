#include <unistd.h>

int		ft_str_is_numeric(char *str)
{
	int flag;

	flag = 1;
	while (*str != '\0')
	{
		if (*str < 48 || *str > 57)
		{
			flag = 0;
		}
		str = str + 1;
	}
	return (flag);
}
