#include <unistd.h>

int		ft_str_is_printable(char *str)
{
	int i;

	i = 1;
	while ((*str != '\0') && (i == 1))
	{
		if (*str >= 32 && *str < 127)
			str = str + 1;
		else
			i = 0;
	}
	return (i);
}
