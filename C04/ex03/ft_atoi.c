int	ft_atoi(char *str)
{
	int i;
	int n;
	int v;

	i = 0;
	n = 0;
	v = 1;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13)
	|| (str[i] == '-' || str[i] == '+'))
	{
		if (str[i] == '-')
			v *= -1;
		else if (str[i] == '+')
			v *= 1;
		i++;
	}
	while ((str[i] != '\0') && str[i] <= '9' && str[i] >= '0')
	{
		n = n * 10 + str[i] - '0';
		i++;
	}
	return (n * v);
}
