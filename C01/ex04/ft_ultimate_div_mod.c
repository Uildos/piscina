#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int tp1;
	int tp2;

	tp1 = *a;
	tp2 = *b;
	*a = tp1 / tp2;
	*b = tp1 % tp2;
}
